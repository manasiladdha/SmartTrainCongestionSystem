package Business.TrainManagementSystem;

/**
 *
 * @author Manasi Laddha
 */
public class TrainCarReport {
    
    private int emptyReservedSeats;
    private int emptyNormalSeats;
    private int emptySpaceToStand;
    private int extraTravellers;

    public int getEmptyReservedSeats() {
        return emptyReservedSeats;
    }

    public void setEmptyReservedSeats(int emptyReservedSeats) {
        this.emptyReservedSeats = emptyReservedSeats;
    }

    public int getEmptyNormalSeats() {
        return emptyNormalSeats;
    }

    public void setEmptyNormalSeats(int emptyNormalSeats) {
        this.emptyNormalSeats = emptyNormalSeats;
    }

    public int getEmptySpaceToStand() {
        return emptySpaceToStand;
    }

    public void setEmptySpaceToStand(int emptySpaceToStand) {
        this.emptySpaceToStand = emptySpaceToStand;
    }

    public int getExtraTravellers() {
        return extraTravellers;
    }

    public void setExtraTravellers(int extraTravellers) {
        this.extraTravellers = extraTravellers;
    }
    
        


    
    
    
}
